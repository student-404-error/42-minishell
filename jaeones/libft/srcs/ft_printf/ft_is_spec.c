#include "ft_printf.h"

int	ft_is_spec(char c)
{
	if (c == '%' || c == 'd' || c == 'i' || c == 'u' || c == 'x'
		|| c == 'X' || c == 'c' || c == 's' || c == 'p')
		return (1);
	return (0);
}
